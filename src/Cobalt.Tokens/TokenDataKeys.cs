namespace Cobalt.Tokens
{
    /// <summary>
    /// Keys for custom token data.
    /// </summary>
    public static class TokenDataKeys
    {
        /// <summary>
        /// Key under which to store a Cobalt type.
        /// </summary>
        public static readonly string COBALT_TYPE = "cobalt_type";

        /// <summary>
        /// Key under which to store a literal value.
        /// </summary>
        public static readonly string LITERAL_VALUE = "literal_value";

        /// <summary>
        /// Key under which to store an identifier name.
        /// </summary>
        public static readonly string IDENTIFIER_NAME = "identifier_name";

        /// <summary>
        /// Key under which to store operator precedence.
        /// </summary>
        public static readonly string OPERATOR_PRECEDENCE = "operator_precedence";

        /// <summary>
        /// Key under which to store operator arity.
        /// </summary>
        public static readonly string OPERATOR_ARITY = "operator_arity";
    }
}
